void LcdDigit(uint8_t u8Digit, uint8_t u8XPos, uint8_t u8YPos)
{
  uint8_t data;
  uint8_t u8XOffset, u8YOffset;
  
  u8XOffset = u8XPos * 10;
  u8YOffset = u8YPos * 2;
  
  if(u8XPos > 6)
  {
    u8XOffset +=2;
  }
  else
  {
    if(u8XPos > 3)
    {
      u8XOffset +=1;
    }
  }
  
  u8Digit = u8Digit * 16;
  Lcd_Set_Curr(u8XOffset,u8YOffset);
  for(uint8_t i = 0; i < 8; i++)
  {
    data = CharSet[i + u8Digit];
    LcdSend(data,LCD_DATA);
  }
  
  Lcd_Set_Curr(u8XOffset,u8YOffset + 1);
  for(uint8_t i = 0; i <8; i++)
  {
    data = CharSet[i+8+u8Digit];
    LcdSend(data,LCD_DATA);
  }
  
}

