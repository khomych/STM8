void delay(void)
{
  for(uint16_t i = 0; i < 10000; i++)nop();
}

void Reset_LCD(void)

{

  delay();


  A70_DC_L;
  delay();
  A70_RST_H;
  delay();
  A70_SCE_L;
  delay();
  A70_RST_L;
  delay(); 
  A70_RST_H;
  A70_SCE_H;

}



